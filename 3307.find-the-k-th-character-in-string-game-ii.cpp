class Solution {
public:

    char kthCharacter(long long k, vector<int>& operations) {
        k -= 1;
        int i = 0;
        int inc = 0;
        for(; k; i++, (k >>= 1)) {
            if (!(k&1)) continue;

            if (operations[i]) inc++;
        }

        return 'a' + inc % ('z' - 'a' + 1);
    }
};