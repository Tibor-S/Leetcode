/*

n(n+1)/2 + (n-1)n/2 + (n-2)(n-1)/2 + ... + (n-n+1)(n-n+2)/2
(n(n+1) + (n-1)n + (n-2)(n-1) + (n-3)(n-2) + (n-4)(n-3) + (n-5)(n-4) + ... + (n-n+1)(n-n+2)) / 2
n2+n + n2-n + n2-3n+3 + n2-5n+6 + n2-7n+12 + n2-9n+20 + n2

(n)

((n+1 + n-1)n + (n-1 + n-3)(n-2) + (n-3 + n-5)(n-4) + ... + (n-n+1)(n-n+2)) / 2
(2(n)n + 2(n-4)(n-2) + (2n-8)(n-4) + ... + (n-n+1)(n-n+2)) / 2

*/

class Solution {
public:
    int uniquePaths(int m, int n) {
        long long prod = 1;
        for (long long i = 0; i < m-1; i++) {
            prod *= n + i;
            prod /= i + 1;
        }
        return prod;
    }
};